						Perhimpunan Mahasiswa Bandung (PMB) adalah sebuah organisasi kemahasiswaan ekstra universitas yang telah resmi terdaftar di Departemen Kehakiman sebagai sebuah Badan Hukum No. J.A.5/33/18 Tgl. 27-4-1956. Perhimpunan ini didirikan pada tanggal 17 Maret 1948 di kota Bandung dengan nama Perhimpunan Mahasiswa Indonesia Bandung (PMIB) yang kemudian namanya diubah menjadi Perhimpunan Mahasiswa Bandung (PMB) pada tanggal 21 Desember 1950.</p>

						<p style="text-align: justify; line-height: 22px; text-indent: 0.4in;">Adapun tujuan didirikannya perhimpunan ini adalah untuk membangkitkan dan memperkekal rasa kekeluargaan di antara mahasiswa dari berbagai universitas yang ada di kota Bandung khususnya, turut serta dalam mempertinggi derajat masyarakat Indonesia serta membangkitkan dan memperbesar rasa tanggung jawab terhadap penyempurnaan kemerdekaan nasional. Sejalan dengan diakuinya kemerdekaan Indonesia dimata dunia kata “Indonesia” di dalam nama Perhimpunan Mahasiswa Indonesia Bandung (PMIB) yang digunakan sejak tahun 1948-1950 kemudian di hilangkan.</p>

